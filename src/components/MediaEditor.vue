<template>
    <modal :active="isOpen" @close="close">
        <div class="mm-modal-wrap is-media-editor">
            <header class="mm-modal-header">
                <h4 class="mm-title">
                    Media Properties
                </h4>

                <a class="mm-icon" @click="close">
                    <icon icon="times" size="lg" />
                </a>
            </header>

            <section class="mm-modal-content">
                <errors v-if="anyErrors" :errors="errors" />

                <template v-if="media">
                    <div v-if="isImage(media.extension)" class="mm-media-editor-layout">
                        <div class="mm-media-editor-image">
                            <img :src="media.thumbnail_url" :alt="media.name">
                        </div>

                        <div class="mm-media-editor-fields">
                            <!-- Media name -->
                            <div class="mm-field">
                                <label for="mm_media_name" class="mm-label">Media Name *</label>

                                <div class="mm-control">
                                    <input
                                        id="mm_media_name"
                                        ref="name"
                                        v-model="form.name"
                                        type="text"
                                        class="mm-input"
                                        required
                                        :disabled="form.processing"
                                        @keydown.enter.prevent="submit"
                                    >
                                </div>
                            </div>
                        </div>
                    </div>

                    <template v-else>
                        <!-- Media name -->
                        <div class="mm-field">
                            <label for="mm_media_name" class="mm-label">Media Name *</label>

                            <div class="mm-control">
                                <input
                                    id="mm_media_name"
                                    ref="name"
                                    v-model="form.name"
                                    type="text"
                                    class="mm-input"
                                    required
                                    :disabled="form.processing"
                                    @keydown.enter.prevent="submit"
                                >
                            </div>
                        </div>
                    </template>
                </template>
            </section>

            <footer class="mm-modal-footer">
                <div class="mm-button-group">
                    <a class="mm-button" @click="close">Cancel</a>

                    <a
                        class="mm-button is-confirm"
                        :class="{ 'loading': isProcessing }"
                        :disabled="isProcessing"
                        @click="submit"
                    >Save</a>
                </div>
            </footer>
        </div>
    </modal>
</template>

<script>
import { mapGetters, mapMutations } from 'vuex';
import formMixin from '../mixins/form';

import Errors from './ui/Errors.vue';
import Modal from './ui/Modal.vue';

const initialValues = function () {
    return {
        id: null,
        name: '',
    };
};

export default {

    components: {
        Errors,
        Modal,
    },
    mixins: [ formMixin ],

    data() {
        return {
            method: 'patch',
            form: initialValues(),
        };
    },

    computed: {
        ...mapGetters({
            isOpen: 'mediaManager/mediaEditorIsOpen',
            isImage: 'mediaManager/isImage',

            media: 'mediaManager/mediaEditorItem',
            activeFolderId: 'mediaManager/activeFolderId',
        }),

        action() {
            return '/admin/api/media/' + this.form.id;
        },
    },

    watch: {
        isOpen(isOpen) {
            if (isOpen) {
                this.form = {
                    id: this.media.id,
                    name: this.media.name,
                };

                this.$nextTick(() => this.$refs.name.focus());
            } else {
                this.form = initialValues();
            }
        },
    },

    methods: {
        ...mapMutations({
            close: 'mediaManager/closeMediaEditor',
            updateMedia: 'mediaManager/updateMediaItem',
            updateSelectedMedia: 'mediaManager/updateSelectedMediaItem',
        }),

        onSuccess() {
            let properties = {
                name: this.form.name,
            };

            this.updateMedia({
                folder: this.activeFolderId,
                id: this.form.id,
                properties,
            });

            this.updateSelectedMedia({
                id: this.form.id,
                properties,
            });

            this.close();
        },
    },
};
</script>
